package assignment_2;

public class Player {

    private String name;
    private int score;

    // Constructor to set player name and initialize score
    public Player(String name) {
        this.name = name;
        this.score = 0;
    }

    public String getName() {
        return name;
    }

    public int getScore() {
        return score;
    }

    // Increment score after correct answer
    public void incrementScore() {
        score++;
    }
}
